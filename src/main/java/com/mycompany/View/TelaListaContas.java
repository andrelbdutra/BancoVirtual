    public void adicionaTabela(){
        // Cria uma c√≥pia da lista de contas para a tabela.
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        List<Conta> list = new ListaConta().getContas();
        Object addLinha[] = new Object[3];
        for (int i = 0; i < Conta.getQuantidadeContas(); i++) {
            addLinha[0] = i;
            addLinha[1] = new ListaConta().getContas().get(i).getCliente().getNomeCompleto();
            addLinha[2] = new ListaConta().getContas().get(i).getSaldo();
            model.addRow(addLinha);
        }
    }
